<script lang="js">
import OutcomeService from '@/shared/services/outcome_service';
import EventService from '@/shared/services/event_service';

import { pick } from 'lodash-es';

export default {
  props: {
    event: Object,
    eventable: Object
  },

  computed: {
    dockActions() {
      return OutcomeService.actions(this.eventable, this);
    },
    menuActions() {
      return pick(EventService.actions(this.event, this), ['pin_event', 'unpin_event', 'notification_history']);
    }
  }
};

</script>

<template>

<section class="outcome-created strand-item__outcome-created" id="'outcome-'+ eventable.id">
  <strand-item-headline :event="event" :eventable="eventable"></strand-item-headline>
</section>
</template>
